/* 
Questão 26: Como você pode usar o operador ternário em JavaScript? 
*/

var notaDoAluno = 8;
var avaliacao = notaDoAluno > 6 ? "aprovado" : "reprovado";
                            //  ? verifica se 6 é < a notaDoAluno, se sim "aprovado" se não "reprovado"
console.log(avaliacao) // output: "aprovado"